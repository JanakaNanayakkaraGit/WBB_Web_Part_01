                            නිර්ණායක මත පදනම් වූ "සමාජ ලේඛනය හෝ ඒකාබද්ධ සුබසාධන ප්‍රතිලාභ දත්ත පද්ධතිය" නිර්මාණය කිරීමේ පළමු පියවර ලෙස විනිවිද පෙනෙන තෝරා ගැනීමේ ක්‍රියාවලියක් යටතේ සියලුම සුබසාධන සහනාධාර ප්‍රතිලාභ ගෙවීම සඳහා සුදුසුකම් ලත් පුද්ගලයන් හඳුනා ගැනීමට උත්සාහ දරා ඇත.
                            මෙම ප්‍රයත්නය රටේ විවිධ සුබසාධන ප්‍රතිලාභ යෝජනා ක්‍රම ප්‍රතිසංස්කරණය, ශක්තිමත් කිරීම සහ නවීකරණය කරන අතලොස්සෙන් එකකි.
                            ප්‍රතිලාභීන්ගේ ආරක්ෂාව සහතික කරන කාර්යක්ෂම හා ඵලදායී දත්ත පද්ධතියක් නිර්මාණය කිරීමේ මෙහෙවරෙහි යෙදී සිටින අයටද එය සතුටකි.
                            හඳුනාගැනීමේ ක්‍රියාවලියේදී පුද්ගලයන්ගේ සහ පවුල්වල තොරතුරු ඉතා නිවැරදිව හා සත්‍යවාදීව ලබා දීමෙන් සහ ලබා ගැනීමෙන් අවශ්‍ය නිවැරදි කිරීම් සිදුකිරීමට සුබසාධක ප්‍රතිලාභ මණ්ඩලයට හැකියාව ඇත.
